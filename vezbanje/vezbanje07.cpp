#include <iostream>
using namespace std;
 
int main()
{
    int i;                               // deklarisanje varijable i
    cout << "Ispis od 1 do 5" << endl;   // naslov:  endl - prelazak u novi red
    for(i=1; i<=5; ++i)                  // ponavljanje za i=1 do i=5 - pocetak petlje
        cout << "i = " << i << endl;     // ispis vrijednosti i  endl - prelazak u novi red
    return 0;
}
 
